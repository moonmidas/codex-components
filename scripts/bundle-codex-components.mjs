import { existsSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, normalize, relative, resolve } from "node:path";

const projectRoot = resolve(new URL("..", import.meta.url).pathname);
const tweakRoot = join(projectRoot, "tweaks", "codex-components");
const entryFile = join(tweakRoot, "index.source.js");
const outputFile = join(tweakRoot, "index.js");

const requirePattern = /require\(["'](\.{1,2}\/[^"']+)["']\)/g;
const modules = new Map();

function moduleId(filename) {
  return `./${relative(tweakRoot, filename).replaceAll("\\", "/")}`;
}

function resolveModule(fromFile, specifier) {
  const base = resolve(dirname(fromFile), specifier);
  const candidates = [
    base,
    `${base}.js`,
    `${base}.cjs`,
    join(base, "index.js"),
    join(base, "index.cjs"),
  ];
  for (const candidate of candidates) {
    if (existsSync(candidate) && statSync(candidate).isFile()) return normalize(candidate);
  }
  throw new Error(`Unable to resolve ${specifier} from ${fromFile}`);
}

function normalizeRequires(filename, source) {
  return source.replace(requirePattern, (_match, specifier) => {
    const resolved = resolveModule(filename, specifier);
    return `__codexComponentsRequire(${JSON.stringify(moduleId(resolved))})`;
  });
}

function collect(filename) {
  const id = moduleId(filename);
  if (modules.has(id)) return;

  const source = filename.endsWith("core/styles.js")
    ? bundledStylesModule()
    : readFileSync(filename, "utf8");

  const deps = [...source.matchAll(requirePattern)].map((match) => resolveModule(filename, match[1]));
  modules.set(id, normalizeRequires(filename, source));
  for (const dep of deps) collect(dep);
}

function bundledStylesModule() {
  const styleLayerFiles = [
    "theme.css",
    "base.css",
    "primitives.css",
    "toolbar.css",
    "settings.css",
    "media.css",
    "html.css",
  ];
  const css = styleLayerFiles
    .map((file) => {
      const source = readFileSync(join(tweakRoot, "styles", file), "utf8").trim();
      return `/* ${file} */\n${source}`;
    })
    .join("\n\n");

  return `const STYLE_LAYER_FILES = Object.freeze(${JSON.stringify(styleLayerFiles, null, 2)});

function loadComponentCss() {
  return ${JSON.stringify(css)};
}

module.exports = {
  STYLE_LAYER_FILES,
  loadComponentCss,
};
`;
}

function renderBundle() {
  const entrySource = normalizeRequires(entryFile, readFileSync(entryFile, "utf8"));
  const moduleEntries = [...modules.entries()]
    .map(([id, source]) => {
      return `${JSON.stringify(id)}: function(module, exports) {\n${source}\n}`;
    })
    .join(",\n");

  return `// Generated by scripts/bundle-codex-components.mjs. Edit index.source.js and component modules instead.
(function() {
  const __codexComponentsModules = {
${moduleEntries}
  };
  const __codexComponentsCache = Object.create(null);
  function __codexComponentsRequire(id) {
    const factory = __codexComponentsModules[id];
    if (!factory) throw new Error("Codex Components bundle missing module: " + id);
    if (__codexComponentsCache[id]) return __codexComponentsCache[id].exports;
    const module = { exports: {} };
    __codexComponentsCache[id] = module;
    factory(module, module.exports);
    return module.exports;
  }

${entrySource}
})();
`;
}

collect(entryFile);
writeFileSync(outputFile, renderBundle());
